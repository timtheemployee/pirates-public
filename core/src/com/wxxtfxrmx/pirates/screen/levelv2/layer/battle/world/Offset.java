package com.wxxtfxrmx.pirates.screen.levelv2.layer.battle.world;

public class Offset {
    private final float x;
    private final float y;

    public Offset(float x, float y) {
        this.x = x;
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }
}
